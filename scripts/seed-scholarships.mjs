async function main() {
  console.log(`\n🌱 Seeding ${scholarships.length} scholarships...\n`);

  // Delete existing seeded scholarships (by known slugs)
  const slugs = scholarships.map(s => s.slug);
  const { error: delErr } = await supabase.from('Scholarship').delete().in('slug', slugs);
  if (delErr) console.warn('  ⚠️  Cleanup warning:', delErr.message);
  else console.log('  ✅ Old records cleared');

  // Insert all scholarships
  const { error } = await supabase.from('Scholarship').insert(scholarships);
  if (error) {
    throw new Error(`Insert failed: ${error.message} | code: ${error.code}`);
  }

  console.log(`  ✅ ${scholarships.length} scholarships seeded\n`);
  console.log('='.repeat(50));
  scholarships.forEach(s => console.log(`  📚 ${s.title} (${s.country})`));
  console.log('='.repeat(50) + '\n');
}

main().catch(e => {
  console.error('\n❌ Seed failed:', e.message);
  process.exit(1);
});
